package geraarquivos.model;

/**
 *
 * @author tiagokochenborger
 */
public class CadLayout {
    
    private int idCadLayout;
    private String nomeLayout;
    private String modeloArquitetura;
    private String separador;
    private String extensaoArquivo;
    private String saidaArquivo;
    private String quebraLinha;

    /**
     * @return the idCadLayout
     */
    public int getIdCadLayout() {
        return idCadLayout;
    }

    /**
     * @param idCadLayout the idCadLayout to set
     */
    public void setIdCadLayout(int idCadLayout) {
        this.idCadLayout = idCadLayout;
    }

    /**
     * @return the nomeLayout
     */
    public String getNomeLayout() {
        return nomeLayout;
    }

    /**
     * @param nomeLayout the nomeLayout to set
     */
    public void setNomeLayout(String nomeLayout) {
        this.nomeLayout = nomeLayout;
    }

    /**
     * @return the modeloArquitetura
     */
    public String getModeloArquitetura() {
        return modeloArquitetura;
    }

    /**
     * @param modeloArquitetura the modeloArquitetura to set
     */
    public void setModeloArquitetura(String modeloArquitetura) {
        this.modeloArquitetura = modeloArquitetura;
    }

    /**
     * @return the extensaoArquivo
     */
    public String getExtensaoArquivo() {
        return extensaoArquivo;
    }

    /**
     * @param extensaoArquivo the extensaoArquivo to set
     */
    public void setExtensaoArquivo(String extensaoArquivo) {
        this.extensaoArquivo = extensaoArquivo;
    }

    /**
     * @return the saidaArquivo
     */
    public String getSaidaArquivo() {
        return saidaArquivo;
    }

    /**
     * @param saidaArquivo the saidaArquivo to set
     */
    public void setSaidaArquivo(String saidaArquivo) {
        this.saidaArquivo = saidaArquivo;
    }

    /**
     * @return the quebraLinha
     */
    public String getQuebraLinha() {
        return quebraLinha;
    }

    /**
     * @param quebraLinha the quebraLinha to set
     */
    public void setQuebraLinha(String quebraLinha) {
        this.quebraLinha = quebraLinha;
    }

    /**
     * @return the separador
     */
    public String getSeparador() {
        return separador;
    }

    /**
     * @param separador the separador to set
     */
    public void setSeparador(String separador) {
        this.separador = separador;
    }
    
    
}
