    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        
            // o arquivo encontra-se no mesmo diret√≥rio //da aplica√ß√£o  
            File file = new File("resources/Config.properties");      
            Properties props = new Properties();  
            FileInputStream fis = null;  
            try {  
                fis = new FileInputStream(file);  
                //l√™ os dados que est√£o no arquivo  
                props.load(fis);    
                fis.close();  
            }  
            catch (IOException ex) {  
                System.out.println(ex.getMessage());  
                ex.printStackTrace();  
            }
            
            
            props.setProperty("server",tfServer.getText());
            props.setProperty("porta",ftPorta.getText());
            props.setProperty("usuario",tfUsuario.getText());
            props.setProperty("senha",pfSenha.getPassword().toString());
            props.setProperty("database",tfNomeBanco.getText());
            //convertendo o item selecionado em texto
            props.setProperty("banco", (String) cbTipo.getSelectedItem());
            
            //o arquivo encontra-se no mesmo diret√≥rio da aplica√ß√£o  
            FileOutputStream fos = null;  
            try {  
                fos = new FileOutputStream(file);  
                //grava os dados  no arquivo  
                props.store(fos, "Configura√ß√µes salvas");    
                fos.close();  
            }  
            catch (IOException ex) {  
                System.out.println(ex.getMessage());  
                ex.printStackTrace();  
            }
            
            
            dispose();
            
        
    }//GEN-LAST:event_btSalvarActionPerformed

